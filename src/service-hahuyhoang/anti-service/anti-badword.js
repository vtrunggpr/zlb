// H√†m ƒë·ªçc danh s√°ch badwords t·ª´ file config
async function loadConfig() {
  const antiState = getAntiState();
  return antiState.data.badWords || [];
}

// H√†m ki·ªÉm tra t·ª´ c·∫•m
async function checkBadWords(content) {
  // Load danh s√°ch t·ª´ c·∫•m m·ªói khi c·∫ßn ki·ªÉm tra
  const badWords = await loadConfig();
  const normalizedContent = normalizeText(content);
  const words = normalizedContent.split(/\s+/);

  for (const badWord of badWords) {
    const normalizedBadWord = normalizeText(badWord);

    // Ki·ªÉm tra t·ª´ng t·ª´ ri√™ng bi·ªát
    for (const word of words) {
      if (word === normalizedBadWord) {
        return {
          found: true,
          word: badWord,
        };
      }
    }

    // Ki·ªÉm tra c·ª•m t·ª´ trong n·ªôi dung
    if (normalizedBadWord.includes(" ")) {
      if (normalizedContent.includes(normalizedBadWord)) {
        return {
          found: true,
          word: badWord,
        };
      }
    }
  }

  return {
    found: false,
    word: null,
  };
}

// H√†m x·ª≠ l√Ω th√™m/x√≥a t·ª´ c·∫•m
async function handleBadWordModification(api, message, action, word) {
  const threadId = message.threadId;

  if (!word) {
    await api.sendMessage(
      {
        msg: `Vui l√≤ng nh·∫≠p t·ª´ kh√≥a c·∫ßn ${action === "add" ? "th√™m" : "x√≥a"}`,
        quote: message,
      },
      threadId,
      message.type
    );
    return;
  }

  const antiState = getAntiState();
  const currentBadWords = [...antiState.data.badWords]; // T·∫°o b·∫£n sao c·ªßa m·∫£ng hi·ªán t·∫°i

  const normalizedWord = word.toLowerCase();

  if (action === "add") {
    if (!currentBadWords.map(w => w.toLowerCase()).includes(normalizedWord)) {
      currentBadWords.push(word);
      await updateAntiConfig({
        ...antiState.data,
        badWords: currentBadWords,
      });

      await api.sendMessage(
        {
          msg: `ƒê√£ th√™m "${word}" v√†o danh s√°ch t·ª´ c·∫•m`,
          quote: message,
          ttl: 30000,
        },
        threadId,
        message.type
      );
    } else {
      await api.sendMessage(
        {
          msg: `T·ª´ "${word}" ƒë√£ c√≥ trong danh s√°ch t·ª´ c·∫•m`,
          quote: message,
          ttl: 30000,
        },
        threadId,
        message.type
      );
    }
  } else if (action === "remove") {
    const index = currentBadWords.map(w => w.toLowerCase()).indexOf(normalizedWord);
    if (index !== -1) {
      currentBadWords.splice(index, 1);
      await updateAntiConfig({
        ...antiState.data,
        badWords: currentBadWords,
      });

      await api.sendMessage(
        {
          msg: `ƒê√£ x√≥a "${word}" kh·ªèi danh s√°ch t·ª´ c·∫•m`,
          quote: message,
          ttl: 30000,
        },
        threadId,
        message.type
      );
    } else {
      await api.sendMessage(
        {
          msg: `Kh√¥ng t√¨m th·∫•y "${word}" trong danh s√°ch t·ª´ c·∫•m`,
          quote: message,
          ttl: 30000,
        },
        threadId,
        message.type
      );
    }
  }
}
